import numpy as np
import pandas as pd
from sklearn.model_selection import train_test_split
from sklearn.feature_extraction.text import TfidfVectorizer
from sklearn.linear_model import LogisticRegression
from sklearn.metrics import accuracy_score

"""# data collection and preprocessing"""

#loading the data from csv file to a pandas dataframe
raw_mail_data = pd.read_csv('/content/mail_data.csv')

"""# Replace missing values"""

# replace the mail values with null string
mail_data = raw_mail_data.where((pd.notnull(raw_mail_data)),'')

# print first 5 rows
mail_data.head()

# checking size of dataset
mail_data.shape

"""# Label Encoding"""

# label spam mail as zero , ham mail as 1
mail_data.loc[mail_data['Category'] == 'spam' , 'Category' ,] = 0
mail_data.loc[mail_data['Category'] == 'ham' , 'Category' ,] = 1

"""spam--- 0

ham--- 1
"""

# separateing data as text and label

x = mail_data['Message']
y = mail_data['Category']

print(x)

print(y)

"""# train test split"""

x_train,x_test,y_train,y_test = train_test_split(x,y,test_size=0.2,random_state=3)

print(x.shape)
print(x_train.shape)
print(x_test.shape)

"""# Feature Extraction"""

# transform text data to vectors that can be used as input to the logistic regression
feature_extraction = TfidfVectorizer(min_df = 1 , stop_words='english' , lowercase = True )

x_train_features = feature_extraction.fit_transform(x_train)
x_test_features = feature_extraction.transform(x_test)

# convert y_train and y_test values as integers
y_train = y_train.astype('int')
y_test = y_test.astype('int')

print(x_train_features)

"""# training  the model

logistic regression
"""

model = LogisticRegression()

# training the logistic regression model with training data
model.fit(x_train_features,y_train)

"""# evaluate trained model"""

prediction_on_training_data = model.predict(x_train_features)
accuracy_on_training_data = accuracy_score(y_train,prediction_on_training_data)

print('Accuracy on training data : ' , accuracy_on_training_data)

# prediction on test data
prediction_on_test_data = model.predict(x_test_features)
accuracy_on_test_data = accuracy_score(y_test,prediction_on_test_data)

print('accuracy on test data : ' , accuracy_on_test_data)

"""# predictive system"""

input_mail = ["Even my brother is not like to speak with me. They treat me like aids patent."]

# convert text to feature vectors
input_data_features = feature_extraction.transform(input_mail)

# making prediction
prediction = model.predict(input_data_features)
print(prediction)

if (prediction[0] == 1):
  print('ham mail')
else:
  print('spam mail')